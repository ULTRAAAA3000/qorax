-- ============================================================
-- QORAX — Migration 0032: Maintenance mode
-- ============================================================
-- Дозволяє власнику сайту позначити "планове обслуговування" —
-- на цей період перевірки продовжуються (дані пишуться в
-- uptime_checks як завжди, щоб не втрачати історію), але:
--   - не відкриваються/закриваються uptime_incidents
--   - не надсилаються down/recovered алерти
--   - в UI показується статус "На обслуговуванні" замість "Недоступний"
--
-- maintenance_until:
--   null                — не на обслуговуванні (за замовчуванням)
--   timestamptz у майбутньому — обслуговування активне до цього часу
--   timestamptz у минулому — вікно закінчилось, вважається як null

alter table sites
  add column if not exists maintenance_until timestamptz;

comment on column sites.maintenance_until is
  'Якщо задано і в майбутньому — сайт на плановому обслуговуванні: перевірки тривають, але інциденти/алерти не створюються. NULL або час у минулому = звичайний режим.';
