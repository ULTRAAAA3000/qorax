-- 0033_extend_member_role_enum.sql
-- Розширюємо enum member_role: додаємо 'editor' і 'viewer' для системи
-- запрошень тимейтів (наступна міграція 0033_team_invites.sql).
--
-- ВАЖЛИВО: ALTER TYPE ... ADD VALUE не можна використати в тій самій
-- транзакції, де нове значення одразу застосовується (наприклад як
-- DEFAULT в CREATE TABLE) — Postgres кине помилку "unsafe use of new
-- value". Тому це окрема міграція: спочатку додаємо значення і комітимо,
-- потім у 0033 використовуємо їх.
--
-- Postgres не дозволяє видаляти значення з enum, тому 'member'
-- лишається в типі як застаріле — нові запрошення його більше не
-- використовують, але існуючі рядки (якщо є) лишаються валідними.

alter type member_role add value if not exists 'editor';
alter type member_role add value if not exists 'viewer';

comment on type member_role is 'owner: власник організації (один на організацію, повний доступ). admin: додатковий власник (сайти, тимейти, білінг). editor: керує сайтами, не бачить білінг і не запрошує людей. viewer: тільки перегляд. member: застаріле значення, нові запрошення його не використовують.';
